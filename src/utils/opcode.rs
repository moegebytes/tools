pub fn opcode_size(op: Opcode) -> u32 {
  match op {
    Opcode::InitStack => 3,
    Opcode::Call | Opcode::Jmp | Opcode::Jz => 5,
    Opcode::SysCall => 3,
    Opcode::PushIntI32 | Opcode::PushFloat => 5,
    Opcode::PushIntI16 => 3,
    Opcode::PushIntI8 | Opcode::PushString => 2,
    Opcode::PushGlbvar | Opcode::PushGlbvarTable | Opcode::PopGlbvar | Opcode::PopGlbvarTable => 3,
    Opcode::PushStkvar | Opcode::PushStkvarTable | Opcode::PopStkvar | Opcode::PopStkvarTable => 2,
    _ => 1,
  }
}
